    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int cod = Integer.parseInt(this.jtcod.getText());
            for (Productos productos : Menu.listP) {
                int codP = productos.getCodigo();
                if (cod == codP) {
                    this.jtdescrip.setText(productos.getDescripcion());
                    this.jtprecio.setText(Double.toString(productos.getPrecio()));
                    this.jCBRubro.setSelectedItem(productos.getRubro());
                    this.jtstock.setText(Integer.toString(productos.getStock()));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, " >>> Ingrese un numero <<<");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

