    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (false == ok) {
            int cod = Integer.parseInt(this.jtcod.getText());
            String des = this.jtdescrip.getText();
            double pre = Double.parseDouble(this.jtprecio.getText());
            Categoria ru = (Categoria) this.jCBRubro.getSelectedItem();
            int st = Integer.parseInt(this.jtstock.getText());
            Menu.listP.add(new Productos(cod, des, pre, st, ru));
            JOptionPane.showMessageDialog(this, " >>> Producto agregado <<<");
            ok = true;
        } else {
            JOptionPane.showMessageDialog(this, " >>> Precione el boton de Nuevo <<<");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int cod = Integer.parseInt(this.jtcod.getText());
        for (Productos productos : Menu.listP) {
            int codP = productos.getCodigo();
            if (cod == codP) {
                Menu.listP.remove(productos);
                JOptionPane.showMessageDialog(this, " >>> Producto Eliminado <<<");
                this.jtcod.setText("");
                this.jtdescrip.setText("");
                this.jtprecio.setText("");
                this.jtstock.setText("");
            }
        }


    }//GEN-LAST:event_jButton4ActionPerformed

